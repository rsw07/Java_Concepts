//Single level inheritance ---> Example1

package Inheritance;

public class Father        // Father --> Base/Super/Parent class
{
	public void House()
	{
		System.out.println("House:Bunglow");
	}

	public void Money()
	{
		System.out.println("Money:1lakh");
	}
	
	public void Car()
	{
		System.out.println("Car:Audi");
	}
}
