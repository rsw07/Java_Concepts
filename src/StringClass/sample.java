//String declaration and initialization

package StringClass;

public class sample 
{
	public static void main(String[]args)
	{
		String s1;         //String declaration
		s1 = "hii";        //String initialization
		
		String s2 = "abcd";   //String declaration and initialization without new keyword
		
		System.out.println(s1);
		System.out.println(s2);
		
		String s3= new String("vedant"); //String declaration and initialization using new keyword
		System.out.println(s3);
	}

}
