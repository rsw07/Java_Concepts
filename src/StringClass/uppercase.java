//3.toUpperCase

package StringClass;

public class uppercase
{
	public static void main(String[] args) 
	{
		String s1 = "Rashmi";
		System.out.println(s1.toUpperCase()); //temporary 
		System.out.println(s1);
		
		System.out.println("----------------");
		
		s1 = s1.toUpperCase();   //permanent
		System.out.println(s1);
		System.out.println(s1);
		
	}

}
