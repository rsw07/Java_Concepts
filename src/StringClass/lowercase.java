//3.toLowerCase

package StringClass;

public class lowercase
{
	public static void main(String[] args) 
	{
		String s1 = "Shrutika";
		System.out.println(s1.toLowerCase());  //temporary
		System.out.println(s1);
		
		System.out.println("-----------------------");
		
		s1 = s1.toLowerCase();   //permanent
		System.out.println(s1);
		System.out.println(s1);
		
	}

}
