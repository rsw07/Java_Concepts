package StringClass;

public class endswith
{

	public static void main(String[] args)
	{
		String s1 = "Rashmi";
		System.out.println(s1.endsWith("I"));   //false
		System.out.println(s1.endsWith("i"));   //true
		System.out.println(s1.endsWith(" "));   //false
		
	}

}
