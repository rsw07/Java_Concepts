// indexOf() --> returns the index of first occuring charater

package StringClass;

public class indexof 
{
	public static void main(String[] args) 
	{
		String s = "abcabcab";
		
		System.out.println(s.indexOf('b'));   //1
		System.out.println(s.indexOf('c'));   //2
		System.out.println(s.indexOf('c'));   //2
		
	}

}
